N = int(input())

dp = [0] * 1000001

dp[1] = 1                   # 1
dp[2] = 2                   # 00 11
dp[3] = 3                   # 001 100 111
dp[4] = dp[3] + dp[2]       # 0000 1001 0011 1100 1111
dp[5] = dp[4] + dp[3]       # 00001 10000 11001 10011 00111 11100 11111

if N > 5:
    for i in range(6, N+1):
        dp[i] = (dp[i-1] + dp[i-2]) % 15746

print(dp[N])